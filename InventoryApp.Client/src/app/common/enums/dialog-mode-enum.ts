/**
 * Different modes that a dialog for adding or
 * editing an object can be in.
 */
export enum DialogMode {

  /**
   * Dialog is in add mode, meaning a new record
   * is being constructed.
   */
  add = 0,

  /**
   * Dialog is in edit mode, meaning an existing
   * record is being modified.
   */
  edit = 1
}
